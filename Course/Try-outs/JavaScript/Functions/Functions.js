// Functions let you wrap bits of code up into reusable packages.They are one of the building blocks of JS

// Declaring the function
function name () {
    console.log("Hello world");
}

//Call it
name();

// If you say "name" you'll get the function back


//Example
function sayHi() {
    console,log("Hello!");
    console.log("Goodbye!");
}
// Call it:
sayHi();
// ->
// Hello!
// Goodbye!