// AND, OR and NOT

// Assuming x = 5 and y = 9

// Operator     Name        Example             Result
// &&           AND         x < 10 && x!== 5    false
// ||           OR          y>9 || x === 5      true
// !            NOT         !(x === y)          true


// && = Both sides should be true or both sides should be false
// || = One side should be true and the other should be false
// ! = Whatever is within the brackets is false