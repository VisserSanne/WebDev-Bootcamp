// Variables are simply containers that store values
// They follow this pattern:
var yourVariableName = yourValue;

// They can store all of the values we've seen
var name = "Rusty";
var secretNumber = 73;
var isAdorable = true;

// You can change the variable by for example typing:
name = "Melody"

// Recall the stored value by calling the variable name
name        // would become
"Melody"

"Hello there " + name   // Don't forget the space behind there! This would become
"Hello there Melody"

var num = 37;
num + 3 + 10    // This would become
50

